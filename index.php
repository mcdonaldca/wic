 		<h1>What is Women in Computing?</h1>
 		<p>MSU Women in Computing (MSU WIC) is an organization of students and faculty (both men and women) who work to recruit, support and retain women in computing fields. If you are interested in computing, we would love to see you at our next meeting.</p>
 		<p>We host many events during the semester, including tech talks from <a href="#">visiting companies</a> (every other Tuesday), <a href="#">hackathons</a>, <a href="#">Girl Scout workshops</a>, <a href="#">web development workshops</a>, and more. Check out our <a href="#">calendar</a> to keep up to date.</p>

